    public void getPorts() {

        List<String> serialPorts = arduino.getSerialPorts();
        for (String serialPort : serialPorts) {
            jComboBox1.addItem(serialPort);
        }
    }

    public void dataJL(String dataR) {
        jLabel4.setText(dataR);
        dataTable[1] = dataR;
        read = false;

    }

    public void checkUsers() {

        for (int i = 0; i < jTable1.getRowCount(); i++) {

            String nome = (String) tb.getValueAt(i, 0);
            card = (String) tb.getValueAt(i, 1);
            System.out.println("saida " + nome + " card " + card + " cardLeitor " + data);
            if (data.equals(card)) {
                jLabel5.setText(nome);
                jLabel5.setBackground(Color.green);
                jLabel6.setText("Liberado");
//                audioAcess();
                try {
                    arduino.sendData("l");
                } catch (ArduinoException | SerialPortException ex) {
                    Logger.getLogger(FrameComm.class.getName()).log(Level.SEVERE, null, ex);
                }
                break;

            } else {
                jLabel5.setText("Sem acesso");
                jLabel5.setBackground(Color.red);
                jLabel6.setText("Nao liberado");
            }

        }
    }

  

