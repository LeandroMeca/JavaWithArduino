    SerialPortEventListener listener = new SerialPortEventListener() {

        @Override
        public void serialEvent(SerialPortEvent spe) {

            try {

                if (arduino.isMessageAvailable()) {
                    data = arduino.printMessage();
                    if (read) {

                        if (data.length() > 0) {
                            dataJL(data);
                        }
                    }
                    read = false;

                    checkUsers();
                }
                if (spinner != null) {
                    arduino.sendData("s" + spinner);
                }

            } catch (ArduinoException | SerialPortException ex) {
                Logger.getLogger(FrameComm.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    };

